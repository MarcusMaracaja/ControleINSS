    private void btnCadCriarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadCriarActionPerformed
        // TODO add your handling code here:
        String cadnome = txtCadNome.getText();
        String cadmat = txtCadMat.getText();
        String cadSSDS = jCSSDS.getSelectedItem().toString();
        String cadUF = jCuf.getSelectedItem().toString();
        String setor = txtSetor.getText();
        String pass1 = jPass1.getText();
        String pass2 = jPass2.getText();

        if (cadnome.isEmpty() || cadmat.isEmpty() || cadSSDS.isEmpty() || cadUF.isEmpty() || setor.isEmpty() || pass1.isEmpty() || pass2.isEmpty()) {
            jOptionPane.showMessageDialog(this, "Todos os campos devem ser preenchidos.");

        } else {
            // Verificar se as senhas coincidem
            if (!pass1.equals(pass2)) {
                jOptionPane.showMessageDialog(this, "As senhas não conferem. Por favor, verifique.");
                return; // Abortar o cadastro caso as senhas não coincidam
            }

            // Criar uma string com o formato do usuário
            String usuario = cadmat + ";" + pass1 + ";" + cadSSDS + ";" + cadUF + ";" + cadnome + ";" + setor;

            // Adicionar o usuário ao arquivo de cadastro
            String filePath = "arquivo.txt"; // Substitua pelo caminho correto do seu arquivo
            try (BufferedWriter writer = new BufferedWriter(new FileWriter(filePath, true))) {
                writer.write(usuario);
                writer.newLine();
                jOptionPane.showMessageDialog(this, "Usuário criado com sucesso!");
                jPanelCadastro.setVisible(false);
                jPanelTelaLogin.setVisible(true);
            } catch (IOException e) {
                jOptionPane.showMessageDialog(this, "Ocorreu um erro ao cadastrar o usuário");
            }
        }
    }//GEN-LAST:event_btnCadCriarActionPerformed

