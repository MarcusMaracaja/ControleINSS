    private String pesquisarIP(String arquivo, String terceiroGrupo) throws IOException {
        try (BufferedReader reader = new BufferedReader(new FileReader(arquivo))) {
            String linha;
            while ((linha = reader.readLine()) != null) {
                String[] dados = linha.split(";");
                String ip = dados[0];
                String resultado = dados[1];
                if (ip.endsWith(terceiroGrupo)) {
                    return resultado;
                }
            }
        }

        return null; // Caso o IP não seja encontrado no arquivo
    }

