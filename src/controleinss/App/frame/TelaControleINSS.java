    private void btnAddNoArquivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddNoArquivoActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
        String tombamento = txtTombamento.getText();
        String item = CBoxSelectItem1.getSelectedItem().toString();
        jListItens2.setModel(listaEquip2);

        if (tombamento.length() != 9) {
            JOptionPane.showMessageDialog(this, "O tombamento deve ter exatamente 9 caracteres.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {

            int linha = 9;
            int coluna = 7;
            int i = 1;

            // Verificar a próxima linha vazia
            while (linha <= 16) {
                String conteudoCelula = EditExcelFile.getCellContent("Autoriza2.xlsx", linha, coluna);
                if (conteudoCelula.isEmpty()) {
                    EditExcelFile.editCell("Autoriza2.xlsx", linha, 1, i, "Autoriza2.xlsx");
                    EditExcelFile.editCell("Autoriza2.xlsx", linha, coluna, item, "Autoriza2.xlsx");
                    EditExcelFile.editCell("Autoriza2.xlsx", linha, 3, tombamento, "Autoriza2.xlsx");
                    listaEquip2.addElement(i + "---" + tombamento + "---" + item);
                    jProBar1.setValue(i);
                    break;
                } else {
                    linha++;
                    i++;
                }
                if (i >= 8) {
                    JOptionPane.showMessageDialog(this, "Arquivo Cheio, imprima e inicie um novo arquivo");
                    // Iniciar um novo arquivo ou realizar ações apropriadas
                }
            }
            txtTombamento.setText("");
        }
    }//GEN-LAST:event_btnAddNoArquivoActionPerformed

